"""
backend/models/db/models.py — SQLAlchemy ORM models.

All primary keys are UUID strings generated by the application layer
(uuid.uuid4()) before insertion so that async SQLAlchemy never needs to
round-trip to the DB just to learn the PK.
"""
from __future__ import annotations

import uuid
from datetime import datetime

from sqlalchemy import (
    BigInteger,
    Boolean,
    DateTime,
    Float,
    ForeignKey,
    Integer,
    String,
    Text,
    func,
)
from sqlalchemy.dialects.postgresql import JSONB, UUID
from sqlalchemy.orm import Mapped, mapped_column, relationship

from backend.models.db.base import Base


# ---------------------------------------------------------------------------
# StoredFile
# ---------------------------------------------------------------------------

class StoredFile(Base):
    __tablename__ = "stored_files"

    id: Mapped[str] = mapped_column(
        UUID(as_uuid=False), primary_key=True, default=lambda: str(uuid.uuid4())
    )
    original_name: Mapped[str] = mapped_column(String(512), nullable=False)
    storage_key: Mapped[str] = mapped_column(String(1024), nullable=False)
    storage_backend: Mapped[str] = mapped_column(String(64), nullable=False, default="local")
    content_type: Mapped[str | None] = mapped_column(String(256))
    size_bytes: Mapped[int | None] = mapped_column(BigInteger)
    uploaded_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), server_default=func.now()
    )


