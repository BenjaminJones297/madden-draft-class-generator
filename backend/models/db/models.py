    draft_class_id: Mapped[str] = mapped_column(
        UUID(as_uuid=False),
        ForeignKey("draft_classes.id", ondelete="CASCADE"),
        primary_key=True,
    )
    prospect_id: Mapped[str] = mapped_column(
        UUID(as_uuid=False),
        ForeignKey("prospects.id", ondelete="CASCADE"),
        primary_key=True,
    )
    rating_id: Mapped[str | None] = mapped_column(
        UUID(as_uuid=False), ForeignKey("prospect_ratings.id", ondelete="SET NULL")
    )
    board_position: Mapped[int | None] = mapped_column(Integer)

    draft_class: Mapped["DraftClass"] = relationship(
        "DraftClass", back_populates="draft_class_prospects"
    )
    prospect: Mapped["Prospect"] = relationship("Prospect", lazy="noload")
    rating: Mapped["ProspectRating | None"] = relationship("ProspectRating", lazy="noload")


